package com.ojas.ra.mapper;

import java.io.Serializable;

import org.codehaus.jackson.annotate.JsonProperty;
@SuppressWarnings("serial")
public class ObjectMapper3 implements Serializable  {
	
	@JsonProperty("technology")
	private String technology;
	@JsonProperty("requirement")
	private int requirement;
	@JsonProperty("fulfilledPositions")
	 private int fulfilledPositions;
	
	
	public ObjectMapper3(String technology, int requirement, int fulfilledPositions) {
		super();
		this.technology = technology;
		this.requirement = requirement;
		this.fulfilledPositions = fulfilledPositions;
	}
	/**
	 * @return the technology
	 */
	public String getTechnology() {
		return technology;
	}
	/**
	 * @param technology the technology to set
	 */
	public void setTechnology(String technology) {
		this.technology = technology;
	}
	/**
	 * @return the requirement
	 */
	public int getRequirement() {
		return requirement;
	}
	/**
	 * @param requirement the requirement to set
	 */
	public void setRequirement(int requirement) {
		this.requirement = requirement;
	}
	/**
	 * @return the fulfilledPositions
	 */
	public int getFulfilledPositions() {
		return fulfilledPositions;
	}
	/**
	 * @param fulfilledPositions the fulfilledPositions to set
	 */
	public void setFulfilledPositions(int fulfilledPositions) {
		this.fulfilledPositions = fulfilledPositions;
	}
	
	

}
